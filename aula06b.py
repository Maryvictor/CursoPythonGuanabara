n = (input('Digite algo'))
print('É um alfanumerico', n.isalnum())
print('É uma letra do alfabeto', n.isalpha())
print('É um ascii', n.isascii())
print('É um decimal', n.isdecimal())
print('É um digito', n.isdigit())
print('É um identificador', n.isidentifier())
print('É tudo letra minuscula', n.islower())
print('São numeros', n.isnumeric())
print('É mostrado na tela', n.isprintable())
print('É um espaço em branco', n.isspace())
print('É um titulo', n.istitle())
print('É tudo letra maiuscula', n.isupper())











